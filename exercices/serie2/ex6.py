#!/usr/bin/env python3

nb = int(input("Entrez un nombre de 1 Ã  7 : "))
if nb == 1:
    print("Lundi")
elif nb == 2:
    print("Mardi")
elif nb == 3:
    print("Mercredi")
elif nb == 4:
    print("Jeudi")
elif nb == 5:
    print("Vendredi")
elif nb == 6:
    print("Samedi")
elif nb == 7:
    print("Dimanche")
else:
    print("Erreur : {} n'est pas un chiffre compris entre 1 et 7 !".format(nb))
